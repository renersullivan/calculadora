class Calculadora:
    def soma():
        num1 = int(input('diga um numero: '))
        num2 = int(input('diga outro numero: '))
        resultado = num1 + num2
        print('o valor da Soma entre {} e {} é igual a {} .'.format(num1,num2,resultado))

    def subtracao():
        num1 = int(input('diga um numero: '))
        num2 = int(input('diga outro numero: '))
        resultado = num1 - num2
        print('o valor da Subtração entre {} e {} é igual a  {} .'.format(num1,num2,resultado))

    def multiplicacao():
        num1 = int(input('diga um numero: '))
        num2 = int(input('diga outro numero: '))
        resultado = num1 * num2
        print('o valor da Multiplicação  entre {} e {}  é igual a {} .'.format(num1,num2,resultado))   

    def  divisao():
        num1 = int(input('diga um numero: '))
        num2 = int(input('diga outro numero: '))
        resultado = num1 / num2
        print('o valor da Divisão entre {} e {} é igual a {} .'.format(num1,num2,resultado))    